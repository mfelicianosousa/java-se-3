    private void clearsFields( JPanel panel){
        
        Component[] components = panel.getComponents();
        for (int i=0; i < components.length; i++){
           if (components[i] instanceof JTextField){
               JTextField fields = (JTextField) components[i];
               fields.setText("");
           }
       } 
    }
