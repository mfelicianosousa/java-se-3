    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Component[] components = pnlDadosPessoais.getComponents();
    
       int contaJTextField = 0, contaJLabel = 0;
       
       for (int i=0; i <components.length; i++){
           if (components[i] instanceof JTextField){
               contaJTextField++;
           } else if (components[i] instanceof JLabel){
              contaJLabel++;
           }
       }
       
       JOptionPane.showMessageDialog(null,"JTextField: "+contaJTextField + "\nJLabel: "+contaJLabel);       
    }//GEN-LAST:event_jButton1ActionPerformed

